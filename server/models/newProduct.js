import mongoose from 'mongoose';

const productSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0
    },
    createdAt: {
        type: Date,
        default: new Date()
    },
});

const NewProduct = mongoose.model('NewProduct', productSchema);

export default NewProduct;